def sum_num(a, b):
    """
    :param a: first number of sum, int
    :param b: second number of sum
    :return:
    """
    c = a + b

    return c

def generate_list(a, b, c, d):

    new_list = []
    new_list.append(a)
    new_list.append(b)
    new_list.append(c)
    new_list.append(d)

    return new_list